import Foundation

private enum AdapterMappingCodingKeys: String, CodingKey {
    case canonicalID
    case method
    case nativeKeys
    case menuTitle
    case menuPath
    case axAction
    case context
    case matchReason
    case isEnabled
}

/// A per-app translation table: maps canonical shortcut IDs to native actions.
///
/// Each adapter targets a single application (by bundle ID) or web app
/// (by domain). When the user presses a canonical shortcut while that app
/// is frontmost, the engine looks up the mapping and executes the native action.
public struct Adapter: Codable, Equatable, Identifiable {
    /// The app's bundle identifier (e.g., "com.microsoft.VSCode")
    /// or a web-app prefixed domain (e.g., "web:notion.so").
    public let appIdentifier: String

    /// Human-readable app name for the UI.
    public let appName: String

    /// Where this adapter came from.
    public let source: Source

    /// The individual shortcut mappings.
    public var mappings: [Mapping]

    public var id: String { appIdentifier }

    public init(
        appIdentifier: String,
        appName: String,
        source: Source = .builtin,
        mappings: [Mapping]
    ) {
        self.appIdentifier = appIdentifier
        self.appName = appName
        self.source = source
        self.mappings = mappings
    }
}

// MARK: - Source

extension Adapter {
    /// How this adapter was created — used for trust and priority ordering.
    public enum Source: String, Codable, Hashable {
        /// Shipped with the app, hand-verified.
        case builtin
        /// Auto-generated by menu introspection (Phase 2).
        case menuIntrospection
        /// Generated by LLM adapter generator (Phase 4, future).
        case llmGenerated
        /// Downloaded from community repository (Phase 5, future).
        case community
        /// Created or edited by the user.
        case user
    }
}

// MARK: - Mapping

extension Adapter {
    /// A single canonical → native translation within an adapter.
    public struct Mapping: Codable, Equatable, Identifiable {
        /// The canonical shortcut ID this mapping handles (e.g., "focus_url_bar").
        public let canonicalID: String

        /// How to execute the native action.
        public let method: Method

        /// For `.keyRemap`: the native key combo to send instead.
        public let nativeKeys: KeyCombo?

        /// For `.menuInvoke`: the menu item title to trigger via AX.
        public let menuTitle: String?

        /// For `.menuInvoke`: the full menu path, for example ["File", "New Window"].
        public let menuPath: [String]?

        /// For `.axAction`: the AX action string (e.g., "AXPress").
        public let axAction: String?

        /// Optional context filter — only apply when this UI context is active.
        /// e.g., "editor", "terminal", "sidebar" (Phase 2+).
        public let context: String?

        /// Human-readable reason for generated mappings, if known.
        public let matchReason: String?

        /// Whether this mapping participates in resolution.
        public let isEnabled: Bool

        public var id: String { canonicalID }

        public init(
            canonicalID: String,
            method: Method,
            nativeKeys: KeyCombo? = nil,
            menuTitle: String? = nil,
            menuPath: [String]? = nil,
            axAction: String? = nil,
            context: String? = nil,
            matchReason: String? = nil,
            isEnabled: Bool = true
        ) {
            self.canonicalID = canonicalID
            self.method = method
            self.nativeKeys = nativeKeys
            self.menuTitle = menuTitle
            self.menuPath = menuPath
            self.axAction = axAction
            self.context = context
            self.matchReason = matchReason
            self.isEnabled = isEnabled
        }

        public init(from decoder: Decoder) throws {
            let container = try decoder.container(keyedBy: AdapterMappingCodingKeys.self)
            canonicalID = try container.decode(String.self, forKey: .canonicalID)
            method = try container.decode(Method.self, forKey: .method)
            nativeKeys = try container.decodeIfPresent(KeyCombo.self, forKey: .nativeKeys)
            menuTitle = try container.decodeIfPresent(String.self, forKey: .menuTitle)
            menuPath = try container.decodeIfPresent([String].self, forKey: .menuPath)
            axAction = try container.decodeIfPresent(String.self, forKey: .axAction)
            context = try container.decodeIfPresent(String.self, forKey: .context)
            matchReason = try container.decodeIfPresent(String.self, forKey: .matchReason)
            isEnabled = try container.decodeIfPresent(Bool.self, forKey: .isEnabled) ?? true
        }
    }
}

// MARK: - Method

extension Adapter.Mapping {
    /// The mechanism used to translate a canonical shortcut into a native action.
    public enum Method: String, Codable {
        /// Rewrite the CGEvent to a different keycode+modifiers.
        case keyRemap

        /// Invoke a menu item via AXUIElement (Phase 2).
        case menuInvoke

        /// Perform an arbitrary AX action (Phase 2).
        case axAction

        /// The canonical shortcut is the same as the native one — pass through.
        case passthrough
    }
}
